export type MemberRole = 'OWNER' | 'EDITOR' | 'VIEWER';
export type AssetCategory = 'CASH' | 'INVESTMENT' | 'CRYPTO' | 'REAL_ESTATE' | 'PENSION' | 'LIABILITY';
export type TxType = 'INCOME' | 'EXPENSE' | 'TRANSFER';
export type CategoryType = 'INCOME' | 'EXPENSE' | 'TRANSFER';
export type RecurringFrequency = 'MONTHLY' | 'YEARLY';

export interface Asset {
  id: number;
  householdId: number;
  name: string;
  category: AssetCategory;
  currency: string;
  isLiability: boolean;
  archivedAt: string | null;
  latestSnapshot?: { value: number; valueKRW: number; date: string };
}

export interface AssetSnapshot {
  id: number;
  assetId: number;
  date: string;
  value: number;
  fxRateToKRW: number;
  valueKRW: number;
}

export interface Category {
  id: number;
  householdId: number | null;
  type: CategoryType;
  name: string;
  icon: string;
  isBuiltin: boolean;
}

export interface Transaction {
  id: number;
  householdId: number;
  date: string;
  type: TxType;
  amount: number;
  currency: string;
  memo: string | null;
  categoryId: number | null;
  category?: Category;
  fromAssetId: number | null;
  toAssetId: number | null;
  tags: string[];
  autoGenerated: boolean;
}

export interface RecurringTransaction {
  id: number;
  householdId: number;
  name: string;
  type: TxType;
  amount: number;
  currency: string;
  categoryId: number | null;
  category?: Category;
  fromAssetId: number | null;
  toAssetId: number | null;
  frequency: RecurringFrequency;
  dayOfMonth: number;
  monthOfYear: number | null;
  startDate: string;
  endDate: string | null;
  active: boolean;
  lastRunDate: string | null;
}

export interface Member {
  id: number;
  userId: number;
  user: { id: number; name: string; avatarColor: string; initial: string };
  role: MemberRole;
}

export interface Invitation {
  id: number;
  code: string;
  role: MemberRole;
  expiresAt: string;
  householdId: number;
}
